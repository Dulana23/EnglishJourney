                 <h4>Negetive Sentences</h4>
                 To make a negative sentence, we use do not (don’t) or does not (doesn’t). For he ,she ,it   we use does not..
            
            <h4>Examples:</h4>
                 I do not like milk.<br>
                She does not like milk.<br>
                Easy Tip:<br>
                 After do / does, use the base verb.<br>
                Got it?
                 <br>
                 <h4>Questions in Simple Present</h4>
                 
We use do or does to ask questions.<br>
Just like previous …Use does for he/she/it
                 <h4>Structure:</h4>
                 Do / Does + subject + verb?
                 <h4>Examples:</h4>
                <br> Do you like apples?<br>
