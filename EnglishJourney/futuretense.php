<p>
What is Simple Future Tense?<br>
We use the <b>Simple Future Tense</b> for:
</p>

<ul class="'action">
<li>Actions that will happen in the future</li>
<li>Plans and decisions about later</li>
</ul>

<p>We make Simple Future with:</p>

<p class="stc"> Basic Structure:
          Subject + will + Verb</p>
                 <br><br>

<h5>Examples</h5>

<ul>
<li>I will go home.</li>
<li>She will study tonight.</li>
<li>We will play cricket.</li>
<li>He will help his mother.</li>
</ul>

<p>
Easy tip:<br>
After will, the verb never changes.
