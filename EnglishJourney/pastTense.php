<p>
Think about <b>yesterday.</b>
</p>

<ul>
<li>Did you go to school?</li>
<li>Did you eat rice?</li>
<li>Did you play with your friends?</li>
</ul>

<p>
These actions happened <b>before</b>
</p>

<p>
What is Simple Past Tense?<br>
We use the <b>Simple Past Tenses for:</b>
</p>

<ul>
<li>Actions that happened in the past</li>
<li>Actions that are finished</li>
</ul>

<h3>Look at these examples</h3>

<ol>
<li>I went to school yesterday.</li>
<li>She played cricket.</li>
<li>We watched a movie.</li>
<li>He ate rice.</li>
</ol>

<p>
Easy tip: If the action is <b>over</b>, use past tense.
</p>

<p>
This is the structure of a past tense sentence
</p>

<h3>Subject + Past verb</h3>

<ol>
<li>I played</li>
<li>She read</li>
<li>They went</li>
</ol>

