<h2>Past verbs</h2>

<p>There are two types of Past verbs.</p>

<ol>
<li>Regular verbs</li>
<li>Irregular verbs</li>
</ol>


<h3>Regular verbs (-ed verbs)</h3>

<table>

<tr>
<th>Verb</th>
<th>Past</th>
</tr>

<tr>
<td>play</td>
<td>played</td>
</tr>

<tr>
<td>walk</td>
<td>walked</td>
</tr>

<tr>
<td>watch</td>
<td>watched</td>
</tr>

<tr>
<td>clean</td>
<td>cleaned</td>
</tr>

</table>


<h3>Irregular verbs</h3>

<p>Some verbs change completely.</p>

<table>

<tr>
<th>Verb</th>
<th>Past</th>
</tr>

<tr>
<td>go</td>
<td>went</td>
</tr>

<tr>
<td>eat</td>
<td>ate</td>
</tr>

<tr>
<td>see</td>
<td>saw</td>
</tr>

<tr>
<td>do</td>
<td>did</td>
</tr>

</table>


<h3>Negative Sentences</h3>

<p>
To make a negative sentence in past tense we use
<b>did not (didn't)</b>
</p>

<p><b>Structure</b></p>

<p>Subject + didn't + verb</p>

<h4>Examples</h4>

<ul>
<li>I did not go to school.</li>
<li>She did not eat rice.</li>
</ul>

<p>
Easy Rule: After <b>did not</b> use the <b>base verb</b>
</p>


<h3>Questions in Simple Past Tense</h3>

<p>We use <b>did</b> to ask questions.</p>

<p><b>Structure</b></p>

<p>Did + subject + verb?</p>

<h4>Examples</h4>

<ul>
<li>Did you go to school?</li>
<li>Did she finish homework?</li>
</ul>

